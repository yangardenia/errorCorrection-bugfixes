- (void)insertCoreData:(EventsTool *)events
{
    NSManagedObjectContext *context = [self managedObjectContext];
    
    NSManagedObject *contactInfo = [NSEntityDescription insertNewObjectForEntityForName:@"Events" inManagedObjectContext:context];
    [contactInfo setValue:events.name forKey:@"name"];
    [contactInfo setValue:events.level forKey:@"level"];
    [contactInfo setValue:events.descrip forKey:@"descrip"];

    NSError *error;
    if(![context save:&error])
    {
        NSLog(@"不能保存：%@",[error localizedDescription]);
    }
}
- (NSArray *)dataFetchRequest
{
    NSManagedObjectContext *context = [self managedObjectContext];
    NSFetchRequest *fetchRequest = [[NSFetchRequest alloc] init];
    NSEntityDescription *entity = [NSEntityDescription entityForName:@"Events" inManagedObjectContext:context];
    [fetchRequest setEntity:entity];
    NSError *error;
    NSArray *fetchedObjects = [context executeFetchRequest:fetchRequest error:&error];
    int i = 0;
    NSMutableArray *array = [NSMutableArray array];
    for (NSManagedObject *info in fetchedObjects) {
        i++;
        EventsTool *event = [[EventsTool alloc]init];
